							this.ro.lianxifangshi = true;
							continue;
						}
				}
				





				this.ruleForm.shuliang = 0
				this.ro.shuliang = false;






			}
			
			
			// 获取用户信息
			this.$http({
				url: `${this.$storage.get('sessionTable')}/session`,
				method: "get"
			}).then(({ data }) => {
				if (data && data.code === 0) {
					
					var json = data.data;
