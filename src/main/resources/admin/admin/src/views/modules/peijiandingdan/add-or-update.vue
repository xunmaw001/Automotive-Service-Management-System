						if(o=='shuliang'){
							this.ruleForm.shuliang = obj[o];
							this.ro.shuliang = true;
							continue;
						}
						if(o=='hejijine'){
							this.ruleForm.hejijine = obj[o];
							this.ro.hejijine = true;
							continue;
