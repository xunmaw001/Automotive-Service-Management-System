							this.ruleForm.peijianfenlei = obj[o];
							this.ro.peijianfenlei = true;
							continue;
						}
						if(o=='peijianjiage'){
							this.ruleForm.peijianjiage = obj[o];
							this.ro.peijianjiage = true;
							continue;
